interface MarkdownOptions {
  onOpenPath?: (path: string, kind: "note" | "folder") => void;
  onOpenReport?: (href: string, label?: string) => void;
  onFocusSpecialist?: (id: string) => void;
  knownSpecialistIds: ReadonlySet<string>;
  /** Required for chart blocks that reference a `.data.json` sidecar. When
   *  omitted, ref-based charts fall back to a placeholder. */
  clientSlug?: string;
  notePath?: string;
}

function buildComponents(opts: MarkdownOptions): Components {
  const isReportHref = (href?: string) =>
    Boolean(href?.startsWith("/api/clients/") && href.includes("/reports/"));
  const relativeReportHref = (href?: string) => {
    if (!href || !opts.clientSlug) return null;
    const normalized = href.replace(/^\.\/+/, "");
    if (!/^reports\/.+\.html$/i.test(normalized)) return null;
    return {
      path: normalized,
      href: reportApiPath(opts.clientSlug, normalized),
    };
  };
  const opensAwayFromApp = (href?: string) =>
    Boolean(href?.startsWith("http"));

  return {
  h1: (props) => (
    <h1
      className="mt-6 mb-3 text-base font-medium uppercase tracking-tight text-white"
      {...props}
    />
  ),
  h2: (props) => (
    <h2
      className="mt-5 mb-2 text-[13px] font-medium uppercase tracking-[0.18em] text-white"
      {...props}
    />
  ),
  h3: (props) => (
    <h3
      className="mt-4 mb-1.5 text-[11px] uppercase tracking-[0.2em] text-ash"
      {...props}
    />
  ),
  h4: (props) => (
    <h4 className="mt-3 mb-1 text-[11px] uppercase tracking-wider text-ash" {...props} />
  ),
  p: ({ children, ...props }) => (
    <p className="my-2 text-[13px] leading-6 text-white/85" {...props}>
      {renderInlineChildren(children, opts)}
    </p>
  ),
  ul: (props) => (
    <ul className="my-2 ml-5 list-disc space-y-1 marker:text-ash/60" {...props} />
  ),
  ol: (props) => (
    <ol className="my-2 ml-5 list-decimal space-y-1 marker:text-ash/60" {...props} />
  ),
  li: ({ children, ...props }) => (
    <li className="text-[13px] leading-6 text-white/85" {...props}>
      {renderInlineChildren(children, opts)}
    </li>
  ),
  a: ({ href, children, ...props }) => {
    if (href?.startsWith("vault:")) {
      const rawTarget = decodeURIComponent(href.slice("vault:".length));
      const target = rawTarget.endsWith("/")
        ? { kind: "folder" as const, path: rawTarget.replace(/\/$/, "") }
        : { kind: "note" as const, path: rawTarget };
      if (opts.onOpenPath) {
        return renderTargetButton(target, children, opts);
      }
      return <strong className="font-semibold text-white">{children}</strong>;
    }
    const reportHref = relativeReportHref(href);
    if ((isReportHref(href) || reportHref) && opts.onOpenReport) {
      const label = extractText(children) || "Open report";
      return (
        <button
          type="button"
          onClick={() => opts.onOpenReport?.(reportHref?.href ?? href!, label)}
          className="border border-gold/50 bg-gold/10 px-1.5 py-0.5 font-mono text-[12px] text-gold underline decoration-gold/30 underline-offset-2 transition-colors hover:border-gold hover:bg-gold/20"
          title={reportHref?.path ?? label}
        >
          {children}
        </button>
      );
    }
    const openInNewTab = opensAwayFromApp(href);
    return (
      <a
        href={href}
        target={openInNewTab ? "_blank" : undefined}
        rel={openInNewTab ? "noopener noreferrer" : undefined}
        className="text-gold underline decoration-gold/40 underline-offset-2 hover:decoration-gold"
        {...props}
      >
        {children}
      </a>
    );
  },
  strong: ({ children, ...props }) => (
    <strong className="font-semibold text-white" {...props}>
      {renderInlineChildren(children, opts)}
    </strong>
  ),
  em: ({ children, ...props }) => (
    <em className="italic text-white/90" {...props}>
      {renderInlineChildren(children, opts)}
    </em>
  ),
  hr: () => <hr className="my-4 border-t border-graphite" />,
  blockquote: ({ children, ...props }) => (
    <blockquote
      className="my-3 border-l-2 border-graphite pl-3 text-[13px] italic text-ash"
      {...props}
    >
      {renderInlineChildren(children, opts)}
    </blockquote>
  ),
  pre: (props) => {
    // Recognise a ```chart fenced block by peeking at the language tag of
    // the wrapped <code>. When we have it, render an inline SVG chart
    // instead of the raw code chrome. The block JSON is the textContent
    // of the <code>; parse-or-fallback so a malformed spec still renders
    // its source rather than crashing the slide-over.
    const child = (props.children as { props?: { className?: string; children?: ReactNode } } | undefined)?.props;
    const language = child?.className ?? "";
    const isChart = /language-chart\b/.test(language);
    if (isChart && opts.clientSlug && opts.notePath) {
      const text = extractText(child?.children).trim();
      try {
        const spec = JSON.parse(text) as ChartSpec;
        return (
          <ChartBlock
            spec={spec}
            clientSlug={opts.clientSlug}
            notePath={opts.notePath}
          />
        );
      } catch {
        /* fall through to default <pre> rendering so the user can still
         * read the (broken) spec rather than seeing nothing. */
      }
    }
    return (
      <pre
        className="my-3 overflow-x-auto border border-graphite bg-charcoal p-3 text-[12px] leading-5 text-white/90"
        {...props}
      />
    );
  },
  code: ({ className, children, ...rest }) => {
    const isBlock = /language-/.test(className ?? "");
    if (isBlock) {
      // Inside <pre>; let <pre> own the chrome.
      return (
        <code className={`font-mono ${className ?? ""}`} {...rest}>
          {children}
        </code>
      );
    }

    // Inline code: try to promote to a clickable hotspot.
    const text = extractText(children);
    const status = inlineStatusFor(text);
    if (status) {
      return (
        <span
          className={
            "inline-flex items-center border px-1.5 py-0.5 font-mono text-[10px] uppercase tracking-wider " +
            INLINE_STATUS_STYLES[status]
          }
        >
          {status}
        </span>
      );
    }
    const target = detectClickTarget(text, opts.knownSpecialistIds, opts.clientSlug);

    if (target) {
      // Only render the interactive shell when a handler exists; otherwise
      // we'd offer a click that does nothing.
      const interactive =
        target.kind === "specialist"
          ? Boolean(opts.onFocusSpecialist)
          : target.kind === "report"
            ? Boolean(opts.onOpenReport)
          : Boolean(opts.onOpenPath);
      if (interactive) {
        return renderTargetButton(target, children, opts);
      }
    }

    return (
      <code
        className="border border-graphite bg-charcoal px-1 py-0.5 font-mono text-[12px] text-gold"
        {...rest}
      >
        {children}
      </code>
    );
  },
  table: (props) => (
    <div className="my-3 overflow-x-auto border border-graphite">
      <table className="w-full border-collapse text-[12px]" {...props} />
    </div>
  ),
  thead: (props) => <thead className="bg-iron" {...props} />,
  th: (props) => (
    <th
      className="border-b border-graphite px-2 py-1.5 text-left text-[10px] font-medium uppercase tracking-wider text-ash"
      {...props}
    />
  ),
  td: ({ children, ...props }) => (
    <td className="border-b border-graphite/50 px-2 py-1.5 text-[12px] text-white/85" {...props}>
      {renderInlineChildren(children, opts)}
    </td>
  ),
  // remark-gfm task list checkboxes
  input: (props) => {
    if (props.type === "checkbox") {
      return (
        <input
          {...props}
          disabled
          className="mr-2 inline-block accent-gold"
          style={{ transform: "translateY(1px)" }}
        />
      );
    }
    return <input {...props} />;
  },
  };
}

const EMPTY_SPECIALIST_SET: ReadonlySet<string> = new Set();

interface Props {
  source: string;
  /** Caller for vault path clicks. When omitted, paths render as plain
   *  inline code (no button). */
  onOpenPath?: (path: string, kind: "note" | "folder") => void;
  onOpenReport?: (href: string, label?: string) => void;
  /** Caller for specialist-id clicks. When omitted, ids render as plain
   *  inline code. Requires `knownSpecialistIds` to be non-empty. */
  onFocusSpecialist?: (id: string) => void;
  /** The set of valid specialist ids. Passed by the chat layer so we
   *  don't promote unrelated kebab-case strings into clickable hotspots. */
  knownSpecialistIds?: ReadonlySet<string> | ReadonlyArray<string>;
  /** Required for `chart` fenced blocks that reference a `.data.json`
   *  sidecar via `ref`. When omitted, ref-based charts fall back to the
   *  default `<pre>` rendering of the code block. */
  clientSlug?: string;
  notePath?: string;
}

function MarkdownBodyImpl({
  source,
  onOpenPath,
  onOpenReport,
  onFocusSpecialist,
  knownSpecialistIds,
  clientSlug,
  notePath,
}: Props) {
  const processed = useMemo(() => preprocessWikilinks(source), [source]);
  const idSet = useMemo<ReadonlySet<string>>(() => {
    if (!knownSpecialistIds) return EMPTY_SPECIALIST_SET;
    return knownSpecialistIds instanceof Set
      ? knownSpecialistIds
      : new Set(knownSpecialistIds);
  }, [knownSpecialistIds]);
  const components = useMemo(
    () =>
      buildComponents({
        onOpenPath,
        onOpenReport,
        onFocusSpecialist,
        knownSpecialistIds: idSet,
        clientSlug,
        notePath,
      }),
    [onOpenPath, onOpenReport, onFocusSpecialist, idSet, clientSlug, notePath],
  );
  return (
    <div
      className="markdown-body min-w-0"
      style={{
        // Hard break-word so long URLs / unbreakable strings can't escape
        // the container left/right edges. Inline chip buttons also respect
        // this via their own max-w-full. Combined with `min-w-0` this kills
        // the overflow bleed seen when the host is a constrained flex/grid
        // cell (e.g. an OS window body).
        overflowWrap: "anywhere",
        wordBreak: "break-word",
      }}
    >
      <ReactMarkdown remarkPlugins={[remarkGfm]} components={components}>
        {processed}
      </ReactMarkdown>
    </div>
  );
}

/**
 * THE click-lag fix. Without this memo, every ChatPanel re-render (focus
 * change, hover change, tab switch, stream tick, ANY state movement
 * upstream) cascaded through every chat turn: each turn's MarkdownBody
 * re-executed react-markdown's full parse + AST walk, even though the
 * turn's `source` string hadn't changed.
 *
 * For an orchestrator chat with ~100 turns, that meant **100 markdown
 * parses on every click** — the dominant source of perceived lag.
 *
 * Default shallow equality is correct here because ChatPanel feeds
 * stable identities to every prop:
 *   - `source`         — string, compared by value
 *   - `onOpenPath`     — `useCallback([], [])` in ChatPanel → stable
 *   - `onOpenReport`   — `useCallback` stable
 *   - `onFocusSpecialist` — `useCallback` stable
 *   - `knownSpecialistIds` — `useMemo` on `[targets]` → stable across most renders
 *   - `clientSlug`, `notePath` — strings, compared by value
 *
 * Net effect: a click that doesn't change any turn's source produces
 * zero markdown re-parses. Click-lag collapses from O(turns) to O(1).
 */
const MarkdownBody = memo(MarkdownBodyImpl);

export default MarkdownBody;
